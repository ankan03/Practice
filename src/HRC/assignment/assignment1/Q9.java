//We can create string object in 2 ways
//By using String Literal
//By using new Keyword

package HRC.assignment.assignment1;

public class Q9 {
    public static void main(String[] args) {
        //By using String Literal
        String str = "Ankan";
        System.out.println("By using String Literal: "+str);

        //By using new Keyword
        String str1 = new String("Mukherjee");
        System.out.println("By using new Keyword: "+str1);
    }
}
